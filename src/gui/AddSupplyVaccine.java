    private void AddButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButActionPerformed
        String vaccinetype = VaccineType.getSelectedItem().toString();
        String vaccinecentre = VaccineCentre.getSelectedItem().toString();
        String amount = AmountTxt.getText();
        String date = ((JTextField)SupplyDateChooser.getDateEditor().getUiComponent()).getText();
        int supplycode=1000;
        try
        {
            File fs = new File("SupplyVaccine.Txt");
            Scanner s = new Scanner(fs);
            while(s.hasNext())                      // Scan file lines using while loop
            {
                String supply =s.nextLine();
                String[] supplyinfo = supply.split(",");        //Splitting the string into the string array based on the delimeter
                supplycode = Integer.parseInt(supplyinfo[0]);       // String to integer
            }
        }
        catch(FileNotFoundException fe)
        {
            supplycode=1000;                // Setting a default code
        }
        catch(Exception e)
        {
            System.out.println("error");            // Validation
        }
        int newsupplycode = supplycode +1;
        String newsupplycode1 = Integer.toString(newsupplycode);
        
        try
        {
            try (PrintWriter pw = new PrintWriter(new BufferedWriter(new FileWriter("SupplyVaccine.txt",true))))
            {
                if(amount.equals("") || date.equals(""))
                        JOptionPane.showMessageDialog(this,"All details are mandatory","Kindly fill in everything",JOptionPane.INFORMATION_MESSAGE); // Validatiom
                else if(!Pattern.matches("^[0-9]+$",AmountTxt.getText()))
                {
                    JOptionPane.showMessageDialog(this,"Amount Must be Number only","Invalid format",JOptionPane.INFORMATION_MESSAGE);          // VALIDATION
                }
                else
                {
                    pw.println(newsupplycode1+","+vaccinetype+","+vaccinecentre+","+amount+","+date);
                    pw.close();
                    JOptionPane.showMessageDialog(this,"Sucessfully Added Supply Record\nSupply Code for this transaction is "+newsupplycode1,"Successful Message",JOptionPane.PLAIN_MESSAGE);
                    AmountTxt.setText("");
                    SupplyDateChooser.setCalendar(null);
                }
            }
            catch(IOException ieo)
            {
                System.out.println("io error");         // Validation
            }
        }
        catch(Exception e)
        {
            System.out.println("error");        // Validation
        }
    }//GEN-LAST:event_AddButActionPerformed

